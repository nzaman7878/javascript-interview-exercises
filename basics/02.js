// Using a for loop
console.log("using a for loop");

// Loop from 1 to 5
for (let i = 1; i <= 5; i++) {
    console.log(i); // Print the current number
}

// Using a while loop
console.log("using a while loop");

let i = 1; // Initialize i to 1

// Loop while i is less than or equal to 5
while (i <= 5) {
    console.log(i); // Print the current number
    i++; // Increment i by 1
}

// Using a do-while loop
console.log("using a do-while loop");

let j = 1; // Initialize j to 1

// Loop at least once and continue while j is less than or equal to 5
do {
    console.log(j); // Print the current number
    j++; // Increment j by 1
} while (j <= 5);
